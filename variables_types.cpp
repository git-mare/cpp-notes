#include <iostream>

int main() {
    // Integer
    int x = 5;

    // Decimals
    double y = 5.99;

    // Character (single character only)
    char z = 'D';

    // Boolean
    char student = true;
    char teacher = false;

    // String (multiple characters)
    std::string name = "mare";

    // Const (unchangeable variable)
    const double pi = 3.14159;
 
    std::cout << pi << std::endl;
    return 0;
}